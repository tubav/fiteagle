package org.fiteagle.interactors.sfa.getversion;

public class F4FEndorsedTools {
	private String tool_name;
	private String tool_logo;
	private String tool_homepage;
	private String tool_version;
	
	
	public String getTool_name() {
		return tool_name;
	}
	public void setTool_name(String tool_name) {
		this.tool_name = tool_name;
	}
	public String getTool_logo() {
		return tool_logo;
	}
	public void setTool_logo(String tool_logo) {
		this.tool_logo = tool_logo;
	}
	public String getTool_homepage() {
		return tool_homepage;
	}
	public void setTool_homepage(String tool_homepage) {
		this.tool_homepage = tool_homepage;
	}
	public String getTool_version() {
		return tool_version;
	}
	public void setTool_version(String tool_version) {
		this.tool_version = tool_version;
	}

}
